	<h3>Teaching Experience</h3>
	<p>Between September 1999 and July 2001, I worked as a student assistant in the <a href="http://wslc.math.ist.utl.pt">Section of Logic and Computation</a> of the <a href="http://www.math.ist.utl.pt">Department of Mathematics</a> at the <a href="http://www.ist.utl.pt/">Instituto Superior Técnico</a>, in Lisbon, teaching exercise classes in several Programming and related courses.</p>
	<p>As a PhD student, I helped with the exercise classes for two courses at the <a href="http://www.ru.nl">University of Nijmegen</a>: T2 (Complexity Theory for Informatics students) and an optative two-week course on Reflection for Biology students, both in 2003/2004.</p>
	<p>After moving back to Lisbon, I worked for three years as an Invited Assistant Professor, again at the Section of Logic and Computation of the Department of Mathematics of Instituto Superior Técnico, teaching various courses on Programming.</p>
	<p>From October 2007 until August 2009 I was an assistant professor at the <a href="http://www.di.fc.ul.pt">Department of Informatics</a> of the <a href="http://www.fc.ul.pt/">Faculty of Sciences</a> of the <a href="http://www.ul.pt/">University of Lisbon</a>, where I taught the introductory Programming course, as well as several exercise and lab classes in other Programming and Theory of Computation classes.</p>
	<p>From October 2009 to the end of 2013 I taught at the <a href="https://www.enautica.pt/">Escola Superior Náutica Infante D. Henrique</a>, located near Lisbon, and switched back to Mathematics. In those four years, I taught all undergraduate Mathematics courses available (Calculus I &amp; II, Linear Algebra, Numerical Methods, Probabilities and Statistics) and worked on reorganizing the syllabus and producing course notes for several of those courses.</p>
	<p>In 2014 I started (again) as a postdoc at the <a href="https://imada.sdu.dk/">Department of Mathematics and Computer Science</a> of the <a href="https://www.sdu.dk/">University of Southern Denmark</a>, becoming Assistant Professor in 2018 and Associate Professor in 2020. I teach different courses in Programming, Logic and Artificial Intelligence.</p>

